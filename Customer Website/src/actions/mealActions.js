export const listProducts = (keyword = '', pageNumber = '', university="") => async (
  dispatch
) => {
  try {
    dispatch({ type: PRODUCT_LIST_REQUEST })
   
    const { data } = await axios.get(
      `${url}/api/meals/university?keyword=${keyword}&pageNumber=${pageNumber}&university=${university}`
    )

    dispatch({
      type: PRODUCT_LIST_SUCCESS,
      payload: data,
    })
  } catch (error) {
    dispatch({
      type: PRODUCT_LIST_FAIL,
      payload:
        error.response && error.response.data.message
          ? error.response.data.message
          : error.message,
    })
  }
}
